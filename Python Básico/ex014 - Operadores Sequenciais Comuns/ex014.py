#Operadores Sequênciais Comuns
s = [101, 102, 110]
t = (150)
print(101 in s) #True se x for um subconjunto de s
print(101 not in s) #False se x for um subconjunto de s
print(s[0] + t) #Concatenação de s e t
print(s[1] * t) #Concatenação de t cópias de s
print(s[0]) #Caractere de índice i em s
print(len(s)) #Comprimento de s
print(min(s)) #Menor item de s
print(max(s)) #Maior item de s