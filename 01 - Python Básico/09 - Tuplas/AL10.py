#Tuplas
tupla = ()
print(tupla)
print(type(tupla))

tupla01 = ("maça", "banana","cereja")
print(tupla01)
print(type(tupla01))

#Usando o construtor do tipo tuple
x = tuple("Maça")
print(x)

'''Tuplas são sequências imutáveis, tipicamente usadas para armazenar coleções de itens heterogêneos. Elas são aplicadas também quando é necessário utilizar uma sequência imutável de dados homogêneos. Uma tupla pode ser criada de algumas maneiras.'''

