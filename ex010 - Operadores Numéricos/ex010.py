#Adicão
x = (50+50)
print(x)

#Subtração
y = (50-40)
print(y)

#Multiplicação
t = (2*5)
print(t)

#Divisão
p = (5/2)
print(p)

#Divisão Inteira
e = (5//2)
print(e)

#Resto na divisão inteira
f = (5%2)
print(f)

#Valor Absoluto
abs(-2.2)

#Exponenciação
g = (2**10)
print(g)

"""Esses são os operadores numéricos."""